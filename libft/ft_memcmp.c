#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int i;

	i = 0;
	if (n <= 0)
		return (0);
	while (((char *)s1)[i] == ((char *)s2)[i] && i < n-1)
		i++;
	return((char *)s2)[i] - ((char *)s1)[i];
}